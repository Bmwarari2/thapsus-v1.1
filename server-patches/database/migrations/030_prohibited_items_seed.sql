-- One-time guard so re-running the seed doesn't double-write. The
-- target table inherits PK from the original schema; here we just
-- need a uniqueness anchor on (term, jurisdiction, language) for
-- ON CONFLICT to work.
ALTER TABLE public.prohibited_items
  ADD COLUMN IF NOT EXISTS category TEXT,
  ADD COLUMN IF NOT EXISTS risk_level TEXT;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_indexes
    WHERE schemaname = 'public'
      AND indexname  = 'prohibited_items_term_jur_lang_uniq'
  ) THEN
    -- Lower-case the term in the index so 'Power bank' and 'power bank'
    -- don't both seed.
    CREATE UNIQUE INDEX prohibited_items_term_jur_lang_uniq
      ON public.prohibited_items (LOWER(term), jurisdiction, language);
  END IF;
END$$;

