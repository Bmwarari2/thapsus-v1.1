--   - KEPHIS phytosanitary
--   - CITES
--   - UK export controls / open general licences
--
-- Run order: after 000_repair_phase4_tables.sql (which creates the
-- table) and 010_rls_lockdown.sql (which sets the SELECT policy for
-- authenticated users). Idempotent: uses ON CONFLICT DO NOTHING keyed
-- on (term, jurisdiction, language).

